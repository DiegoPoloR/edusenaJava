    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
       
        try {
             Curso curso = new Curso(Integer.parseInt(txtCodigo.getText()),txtNombre.getText(),CMBXProfesor, comboxProf );
             CursoCtrl addCurso = new CursoCtrl();
             boolean agregado = addCurso.setCurso(curso);
             if(agregado)this.ActualizarDatosTabla();           
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Algunos datos estan ingresados de forma erronea, verifiquelos.");  
        }
       
        

    }//GEN-LAST:event_btnAgregarActionPerformed

